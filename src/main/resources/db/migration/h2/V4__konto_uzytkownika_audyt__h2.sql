-- schema: whiteboard_db
CREATE TABLE IF NOT EXISTS konto_uzytkownika_audyt (
                                                       id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                                       konto_id    BIGINT       NOT NULL,
                                                       akcja       VARCHAR(16)  NOT NULL,
                                                       stare       CLOB         NULL,
                                                       nowe        CLOB         NULL,
                                                       autor       VARCHAR(320) NULL,
                                                       utworzono   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_kua_konto ON konto_uzytkownika_audyt(konto_id);
CREATE INDEX IF NOT EXISTS idx_kua_czas  ON konto_uzytkownika_audyt(utworzono);